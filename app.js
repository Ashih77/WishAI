async function loadAppSettings(options = {}) {
    const { force = false } = options;
    const cached = readJsonStorage('wishai_app_settings', null);
    if (!force && cached?.imageModel) {
        state.imageModel = normalizeImageModel(cached.imageModel);
        renderImageModelChoices();
    }

    try {
        const res = await fetch(`/api/app-settings?t=${Date.now()}`, { cache: 'no-store' });
        const data = await res.json();
        if (!data.ok) throw new Error(data.error || 'APP_SETTINGS_FAILED');

        state.imageModel = normalizeImageModel(data.settings?.imageModel);
        localStorage.setItem('wishai_app_settings', JSON.stringify({
            imageModel: state.imageModel,
            updatedAt: data.settings?.updatedAt || null
        }));
    } catch (err) {
        console.warn('Could not load app settings:', err);
        state.imageModel = normalizeImageModel(cached?.imageModel || state.imageModel);
    }

    renderImageModelChoices();
    return state.imageModel;
}

async function flushPendingCloudSaves() {
    const queue = readJsonStorage(PENDING_SAVES_KEY, []);
    if (!queue.length) return;
    const remaining = [];
    for (const item of queue) {
        try {
            const res = await fetch('/api/save-cloud', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(item)
            });
            const data = await res.json();
            if (!data.ok) throw new Error('SAVE_NOT_OK');
            trackAnalytics('cloud_save_retried_success', { fileKey: item.fileKey });
        } catch {
            remaining.push(item);
        }
    }
    localStorage.setItem(PENDING_SAVES_KEY, JSON.stringify(remaining));
}

function shouldRetryRatingSave(err) {
    return [404, 409, 425, 429, 500, 502, 503, 504].includes(err?.status);
}

async function postRatingPayload(payload, options = {}) {
    const attempts = options.attempts || 3;
    let lastError = null;

    for (let attempt = 1; attempt <= attempts; attempt++) {
        try {
            const res = await fetch('/api/save-rating', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(payload)
            });
            const data = await readApiJson(res);
            if (res.ok && data.ok !== false) return data;

            const err = new Error(data.error || `RATING_SAVE_FAILED_HTTP_${res.status}`);
            err.status = res.status;
            throw err;
        } catch (err) {
            lastError = err;
            if (attempt >= attempts || !shouldRetryRatingSave(err)) break;
            await sleep(500 * attempt);
        }
    }

    throw lastError || new Error('RATING_SAVE_FAILED');
}

async function flushPendingRatings() {
    const queue = readJsonStorage(PENDING_RATINGS_KEY, []);
    if (!queue.length) return;

    const remaining = [];
    for (const item of queue) {
        const { ratingKey, queuedAt, ...payload } = item;
        try {
            await postRatingPayload(payload, { attempts: 1 });
            trackAnalytics('rating_retried_success', { fileKey: payload.fileKey, rating: payload.rating, queuedAt });
        } catch (err) {
            remaining.push(item);
        }
    }

    localStorage.setItem(PENDING_RATINGS_KEY, JSON.stringify(remaining));
}

function flushPendingWork() {
    flushPendingCloudSaves();
    flushPendingRatings();
}

function initTheme() {
    const savedTheme = localStorage.getItem('wishai_theme');
    state.theme = savedTheme === 'dark' ? 'dark' : 'light';
    applyTheme();
}

function applyTheme() {
    document.documentElement.dataset.theme = state.theme;
    localStorage.setItem('wishai_theme', state.theme);
    const isDark = state.theme === 'dark';
    const themeColor = document.getElementById('theme-color-meta');
    const appleStatusBar = document.getElementById('apple-status-bar-meta');
    if (themeColor) themeColor.setAttribute('content', isDark ? '#071426' : '#f6f7f4');
    if (appleStatusBar) appleStatusBar.setAttribute('content', isDark ? 'black-translucent' : 'default');
    updateThemeToggleText();
}

function toggleTheme() {
    state.theme = state.theme === 'dark' ? 'light' : 'dark';
    applyTheme();
}

function updateThemeToggleText() {
    const text = document.getElementById('theme-toggle-text');
    const icon = document.getElementById('theme-toggle-icon');
    const btn = document.getElementById('theme-toggle');
    if (!text || !icon || !btn) return;

    const isDark = state.theme === 'dark';
    text.textContent = isDark ? translations[state.lang]['theme-light'] : translations[state.lang]['theme-dark'];
    icon.textContent = isDark ? '☀' : '☾';
    btn.setAttribute('aria-label', text.textContent);
}

function checkAuth() {
    const savedUser = localStorage.getItem('wishai_user');
    if (savedUser) {
        state.isLoggedIn = true;
        state.user = JSON.parse(savedUser);
        updateUserInfo();
        go(1);
    } else {
        go(0);
    }
}

function updateUserInfo() {
    const userInfo = document.getElementById('user-info');
    const nameHeader = document.getElementById('user-name-header');
    if (state.isLoggedIn && state.user) {
        userInfo.classList.remove('hidden');
        nameHeader.textContent = state.user.name;
    } else {
        userInfo.classList.add('hidden');
    }
}

function logout() {
    state.isLoggedIn = false;
    state.user = null;
    localStorage.removeItem('wishai_user');

    // Revoke Google session if needed
    if (typeof google !== 'undefined' && google.accounts.id) {
        google.accounts.id.disableAutoSelect();
    }

    updateUserInfo();
    go(0);
}

