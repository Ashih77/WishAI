async function generate() {
    console.log("🚀 WishAI Radical Engine — Status: IGNITION");

    const img = document.getElementById('generated-image');
    const loading = document.getElementById('loading-area');
    const result = document.getElementById('result-area');
    const greetingOverlay = document.getElementById('overlay-greeting');
    const nameOverlay = document.getElementById('overlay-name');

    // 1. Prepare UI
    img.style.opacity = '0.3';
    loading.classList.remove('hidden');
    result.classList.add('hidden');

    // 2. Build Sophisticated Prompt
    const occ = occasions.find(o => o.id === state.occasion);
    const occDesc = state.lang === 'ar' ? occ.descAr : occ.descEn;
    const langLabel = state.lang === 'ar' ? 'Arabic' : 'English';

    // Build content elements description
    const elemMap = {
        people: state.lang === 'ar' ? 'أشخاص يحتفلون' : 'people celebrating',
        landmarks: state.lang === 'ar' ? 'معالم ومساجد وأماكن دينية' : 'landmarks, mosques, and religious places',
        food: state.lang === 'ar' ? 'طعام وحلويات وكعك المناسبة' : 'food, sweets, and occasion-specific treats',
        decorations: state.lang === 'ar' ? 'زخارف إسلامية وفوانيس وإضاءات احتفالية' : 'Islamic patterns, lanterns, and festive decorations',
        nature: state.lang === 'ar' ? 'طبيعة خلابة وورود وأزهار' : 'beautiful nature, roses, and flowers',
        candles: state.lang === 'ar' ? 'شموع مضيئة وإضاءة دافئة وأنوار رومانسية' : 'glowing candles, warm lighting, and romantic ambiance'
    };
    
    const selectedElements = state.contentElements.map(e => elemMap[e]).join(' AND ');
    const allElements = Object.keys(elemMap);
    const unselectedElements = allElements.filter(e => !state.contentElements.includes(e)).map(e => elemMap[e]).join(', ');

    const elemInstruction = selectedElements 
        ? `CRITICAL REQUIREMENT: You MUST clearly and prominently INCLUDE THESE SPECIFIC ELEMENTS in the image: ${selectedElements}.` 
        : '';
        
    const negativeInstruction = unselectedElements
        ? `CRITICAL REQUIREMENT: You MUST NOT INCLUDE any of the following elements under any circumstances (ignore them even if the occasion usually has them): ${unselectedElements}.`
        : '';

    const posMap = {
        top: 'at the top',
        middle: 'in the middle',
        bottom: 'at the bottom'
    };
    const namePosInstruction = `Write the sender name "${state.name}" only once ${posMap[state.namePosition] || 'at the bottom'}.`;
    
    let textStyleInstruction = '';
    if (state.tashkeel || state.zakhrafa) {
        textStyleInstruction = `CRITICAL TEXT STYLE: The Arabic text must be `;
        if (state.tashkeel) textStyleInstruction += `fully vocalized with precise Arabic diacritics (Tashkeel) `;
        if (state.tashkeel && state.zakhrafa) textStyleInstruction += `and `;
        if (state.zakhrafa) textStyleInstruction += `written in a highly decorative, ornate calligraphy style (Zakhrafa).`;
    }

    let autoCorrections = "";
    try {
        const learnRes = await fetch('/api/get-learnings');
        const learnData = await learnRes.json();
        if (learnData.ok && learnData.rules && learnData.rules.length > 0) {
            autoCorrections = `\n\nCRITICAL AUTO-CORRECTION RULES (Learn from past mistakes):\n` + learnData.rules.join('\n');
        }
    } catch(e) { console.warn("Could not fetch learnings:", e); }

    const prompt = `Create a stunning, high-resolution vertical greeting card.
Occasion Context: ${occDesc}
Style: ${state.style} (${state.subStyle || 'Modern'}).
Quality: Cinematic lighting, 8k, professional.

${elemInstruction}
${negativeInstruction}

Instructions: ${state.instructions || 'None'}.
TEXT TO RENDER ON THE CARD: "${state.greeting}". ${namePosInstruction}
IMPORTANT: Do NOT duplicate any text. Each text element must appear exactly once.
Ensure ${langLabel} text is clear, artistic, and properly connected.
${textStyleInstruction}${autoCorrections}`;

    function showImage(base64, mimeType) {
        const src = `data:${mimeType};base64,${base64}`;
        img.src = src;
        img.style.opacity = '1';
        
        // Hide overlays for Gemini (it draws text)
        document.querySelector('.card-overlay').classList.add('hidden');
        
        loading.classList.add('hidden');
        result.classList.remove('hidden');
        saveCard(src, state.occasion, state.name);
        console.log("✅ Card Generated Successfully via Primary Engine.");
    }

    function showImageUrl(url) {
        const loader = new Image();
        loader.onload = () => {
            img.src = url;
            img.style.opacity = '1';
            
            // Show overlays for Fallback/Flux (it doesn't draw text)
            greetingOverlay.textContent = state.greeting;
            nameOverlay.textContent = state.name;
            document.querySelector('.card-overlay').classList.remove('hidden');
            
            loading.classList.add('hidden');
            result.classList.remove('hidden');
            saveCard(url, state.occasion, state.name);
            console.log("✅ Card Generated Successfully via Fallback Engine.");
        };
        loader.onerror = () => {
            console.error("Fallback URL failed to load.");
            fallback();
        };
        loader.src = url;
    }

    try {
        console.log("🚀 Calling Nano Banana 2 (Imagen 3)...");
        
        const controller = new AbortController();
        const timeout = setTimeout(() => controller.abort(), REQUEST_TIMEOUT); 

        const res = await fetch(API_BASE, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            signal: controller.signal,
            body: JSON.stringify({
                action: 'generate',
                contents: [{ parts: [{ text: prompt }] }]
            })
        });
        clearTimeout(timeout);

        const result = await res.json();
        
        if (result.ok) {
            console.log(`[WishAI] Success using route: ${result.used || 'Direct'}`);
            // Handle Direct Fallback URL
            if (result.imageUrl) {
                showImageUrl(result.imageUrl);
                return;
            }
            
            // Handle Standard Gemini Base64
            const imgPart = result.data.candidates?.[0]?.content?.parts?.find(p => p.inlineData);
            if (imgPart) {
                showImage(imgPart.inlineData.data, imgPart.inlineData.mimeType || 'image/png');
                return;
            }
        }
        
        throw new Error(result.message || result.error || "GEN_FAILED");

    } catch (e) {
        console.warn("Generation Issue:", e.message);
        const errorMsg = document.createElement('div');
        errorMsg.style.cssText = "color:var(--important);background:rgba(0,0,0,0.8);padding:15px;border-radius:10px;text-align:center;margin-top:10px;font-size:0.85rem;";
        errorMsg.innerHTML = `⚠️ <b>عذراً، لم نتمكن من الوصول لنانو بنانا</b><br>تأكد من صلاحية المفتاح والمنطقة الجغرافية.<br><small>السبب: ${e.message}</small>`;
        loading.appendChild(errorMsg);
    }
}




