function normalizeImageModel(model) {
    return Object.prototype.hasOwnProperty.call(IMAGE_MODELS, model) ? model : 'nano-banana-2';
}

function getImageModelLabel(model) {
    return getImageModelLabelForLang(model, state.lang);
}

function getImageModelLabelForLang(model, lang = 'ar') {
    const normalized = normalizeImageModel(model);
    return lang === 'ar' ? IMAGE_MODELS[normalized].labelAr : IMAGE_MODELS[normalized].labelEn;
}

function getGenerationSettingsSnapshot() {
    const imageModel = normalizeImageModel(state.imageModel);
    return {
        imageModel,
        imageModelLabelAr: getImageModelLabelForLang(imageModel, 'ar'),
        imageModelLabelEn: getImageModelLabelForLang(imageModel, 'en')
    };
}

function getRecommendedArtStyleForOccasion(occasionId) {
    const normalizedOccasion = normalizeOccasionId(occasionId);
    return OCCASION_RECOMMENDED_STYLES[normalizedOccasion] || 'modern';
}

function selectArtStyle(style, options = {}) {
    const { recommended = false } = options;
    const styleCard = document.querySelector(`.style-card[data-value="${style}"]`);
    if (!styleCard) return false;

    applyingRecommendedArtStyle = recommended;
    styleCard.click();
    applyingRecommendedArtStyle = false;
    return true;
}

function applyRecommendedArtStyleForOccasion(occasionId) {
    if (userSelectedArtStyle) return;
    selectArtStyle(getRecommendedArtStyleForOccasion(occasionId), { recommended: true });
}

function getAutoSubStylePool(style, occasionId) {
    const normalizedOccasion = normalizeOccasionId(occasionId);
    const stylePools = OCCASION_SUB_STYLE_POOLS[style] || {};
    if (RELIGIOUS_OCCASIONS.has(normalizedOccasion) && stylePools.religious) return stylePools.religious;
    if (stylePools[normalizedOccasion]) return stylePools[normalizedOccasion];
    return stylePools.default || SUB_STYLES_CONFIG[style]?.en || [];
}

function getRandomSubStyle(style, occasionId = state.occasion) {
    const options = getAutoSubStylePool(style, occasionId);
    if (!options.length) return '';
    return options[Math.floor(Math.random() * options.length)];
}

function resolveEffectiveSubStyle() {
    if (userOpenedAdvancedOptions && userSelectedSubStyle && state.subStyle) return state.subStyle;
    return getRandomSubStyle(state.style, state.occasion);
}

