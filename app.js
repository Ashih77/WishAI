function saveCard(src, occId, name, isFallback = false) {
    try {
        // Hide the rating container initially while loading
        document.getElementById('rating-container').classList.add('hidden');
        if (document.getElementById('eval-loading')) {
            document.getElementById('eval-loading').classList.remove('hidden');
        }

        // Generate file key locally if possible to avoid waiting for backend
        const localFileKey = `WishAI-${occId}-${Date.now()}`;
        state.currentFileKey = localFileKey;
        state.shareImageUrl = '';
        state.sharePageUrl = '';
        currentShareSavePromise = null;
        const generationSettings = getGenerationSettingsSnapshot();
        const stateParams = {
            ...JSON.parse(JSON.stringify(state)),
            settings: generationSettings,
            imageModel: generationSettings.imageModel
        };

        // Save to cloud for admin/monitoring
        if (!isFallback && src) {
            const cloudPayload = { fileKey: localFileKey, image: src, stateParams };
            currentShareSavePromise = fetch('/api/save-cloud', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(cloudPayload)
            })
            .then(res => res.json())
            .then(data => {
                if (!data.ok) throw new Error(data.error || 'SAVE_NOT_OK');

                // Remove loading message and present the rating UI now that the backend evaluated it safely
                if (document.getElementById('eval-loading')) {
                    document.getElementById('eval-loading').classList.add('hidden');
                }
                
                // If backend generated a different fileKey, update it
                if (data.ok && data.fileKey) {
                    state.currentFileKey = data.fileKey;
                    state.shareImageUrl = data.imageUrl || `${window.location.origin}/api/get-cloud-image?key=${encodeURIComponent(data.fileKey)}`;
                    state.sharePageUrl = data.shareUrl || `${window.location.origin}/share?id=${encodeURIComponent(data.fileKey)}`;
                    document.getElementById('rating-container').classList.remove('hidden');
                    trackAnalytics('cloud_save_success', { occId, imageModel: generationSettings.imageModel, fileKey: data.fileKey });
                    saveUserAnalytics('generation');
                }
            })
            .catch(err => {
                console.error('Failed to save to cloud:', err);
                queuePendingCloudSave(cloudPayload);
                trackAnalytics('cloud_save_queued', { occId, imageModel: generationSettings.imageModel, fileKey: localFileKey });
                if (document.getElementById('eval-loading')) {
                    document.getElementById('eval-loading').classList.add('hidden');
                }
            });
        } else {
            currentShareSavePromise = null;
        }

        let cards = JSON.parse(localStorage.getItem('wishai_cards') || '[]');
        cards.unshift({
            id: Date.now(),
            src,
            occId,
            name,
            date: new Date().toISOString(),
            isFallback,
            settings: generationSettings,
            state: stateParams
        });
        if (cards.length > 15) cards.pop();

        // Try to save. If it fails due to quota, keep popping until it fits, or abandon.
        let saved = false;
        while (!saved && cards.length > 0) {
            try {
                localStorage.setItem('wishai_cards', JSON.stringify(cards));
                saved = true;
            } catch (e) {
                cards.pop(); // Remove oldest to free space
            }
        }
        trackAnalytics('generation_saved_local', { occId, isFallback, imageModel: generationSettings.imageModel });
    } catch (err) {
        console.warn('Could not save card to history:', err);
    }
}

